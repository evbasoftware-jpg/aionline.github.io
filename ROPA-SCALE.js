const FITMENT = {1990:1.1, 1998:1.25, 2009:1.86, 2019:2.57};

function getScaleIncrement(basic, ropaYear) {
    let cat = el("category").value; // A, B, C বা high
    
    // --- ROPA 2019 লজিক (ছবি অনুযায়ী পে-ম্যাট্রিক্স থেকে পরবর্তী ঘর নির্ধারণ) ---
    if (ropaYear === 2019) {
        let matrix = SCALE[2019][cat];
        if (matrix) {
            // চার্টে বর্তমান বেসিকটি খুঁজে বের করা
            for (let i = 0; i < matrix.length - 1; i++) {
                if (basic === matrix[i]) {
                    return matrix[i + 1]; // ঠিক পরের ধাপটি রিটার্ন করবে
                }
            }
            // যদি চার্টে বেসিকটি হুবহু না পাওয়া যায় (যেমন: মাঝখানে কেউ এডিট করলে)
            // তাহলে ৩% করে পরবর্তী ১০০-তে রাউন্ড করবে
            let nextBasic = Math.ceil((basic * 1.03) / 100) * 100;
            return nextBasic;
        }
    }

    // --- ROPA 2009 লজিক (৩% বৃদ্ধি এবং পরবর্তী ১০-এর গুণিতকে রাউন্ড) ---
    if (ropaYear === 2009) {
        // ROPA 2009 অনুযায়ী ইনক্রিমেন্ট সবসময় পরবর্তী ১০-এর গুণিতকে রাউন্ড হয় (Math.ceil)
        return Math.ceil((basic * 1.03) / 10) * 10;
    }

    // --- পুরোনো ROPA (1981, 1990, 1998) লজিক ---
    let currentScale = SCALE[ropaYear] ? SCALE[ropaYear][cat] : null;
    if (!currentScale) return basic;

    for (let i = 0; i < currentScale.length - 2; i += 2) {
        let stageStart = currentScale[i];
        let stageInc = currentScale[i + 1];
        let stageEnd = currentScale[i + 2];
        
        if (basic >= stageStart && basic < stageEnd) {
            return basic + stageInc;
        }
    }

    // যদি স্কেলের একদম শেষ সীমায় থাকে বা কোনো রেঞ্জে না পড়ে
    return basic;
      }

function getFitmentBasic(basic,oldYear,newYear){
    if(
        typeof FITMENT==="undefined"
    ){
        return basic;
    }
    let factor =
        FITMENT[newYear] || 1;

    return Math.round(
        basic*factor/100
    )*100;
}

function getPromotionBasic(basic,year){
    return getScaleIncrement(
        basic,
        year
    );
}

